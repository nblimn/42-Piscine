#include "ft_stock_str.h"
#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putnbr(nb / 10);
		ft_putchar('8');
	}
	else if (nb < 0)
	{
		ft_putchar('-');
		ft_putnbr(-nb);
	}
	else
	{
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
		}
		ft_putchar(48 + nb % 10);
	}
}

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		ft_putchar(*str);
		str++;
	}
}

void	ft_show_tab(struct s_stock_str *par)
{
	while ((*par).str != 0)
	{
		ft_putstr((*par).str);
		ft_putchar('\n');
		ft_putnbr((*par).size);
		ft_putchar('\n');
		ft_putstr((*par).copy);
		ft_putchar('\n');
		par++;
	}
}
